#import "RMSMusicFile.h"
#import "RMSAudioUnitFilePlayer.h"


////////////////////////////////////////////////////////////////////////////////

@interface RMSMusicFile ()
{
}

@end

////////////////////////////////////////////////////////////////////////////////
@implementation RMSMusicFile
////////////////////////////////////////////////////////////////////////////////

- (BOOL) shouldAddItem:(FSItem *)item
{
	NSString *pathExt = [item.url pathExtension];
	if (pathExt && pathExt.length)
	{
		return [[RMSAudioUnitFilePlayer readableTypes] containsObject:pathExt];
	}
	
	return [item isContainer];
}

////////////////////////////////////////////////////////////////////////////////
@end
