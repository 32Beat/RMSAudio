#import "RMSDevice.h"


@interface RMSDevice ()
{
}
@end

////////////////////////////////////////////////////////////////////////////////
@implementation RMSDevice

+ (instancetype) instanceWithDeviceID:(AudioDeviceID)deviceID
{ return [[self alloc] initWithDeviceID:deviceID]; }

- (instancetype) initWithDeviceID:(AudioDeviceID)deviceID
{
	self = [super init];
	if (self != nil)
	{
		_deviceID = deviceID;
	}
	
	return self;
}

@end
